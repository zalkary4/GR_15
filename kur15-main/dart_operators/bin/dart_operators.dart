void main(List<String> arguments) {
  // `+` koshot
  print(1 + 1);

  // `-` kemituu
  print(1 - 1);

  // `*` koboytuu
  print(2 * 2);

  // `/` boluu
  print(9 / 3);

  // `>` chong belgisi tuura bolso `true` bolboso `false` beret
  print(4 > 2);
  print(2 > 4);

  // `<` kichine belgisi tuura bolso `true` bolboso `false` beret
  print(4 < 2);
  print(2 < 4);

  // `<=` kichine je barabar
  print(4 <= 5);

  // `>=` kichine je barabar
  print(4 >= 5);

  // `!=` barabar emes
  print(4 != 4);
  print(10 != 20);

  // `==` Baraberby
  print(3 == 6);
  print(9 == 9);

  // `&&` jana
  print(3 > 2 && 2 < 4);

  // `||` je (je bolboso)
  print(2 < 1 || 3 > 2);
  print('bir' == 'eki' || 'bir' == '1');
  print('aa' == 'aa' || 'bb' == 'bb');
  print('aa' == 'aA');
  print('a' == 'A');
}
